package br.edu.univas.ed.domino.models;

public class Table {
    private List left;
    private List right;
    private List stock;

    public List getLeft() {
        return this.left;
    }

    public void setLeft(List left) {
        this.left = left;
    }

    public List getRight() {
        return this.right;
    }

    public void setRight(List right) {
        this.right = right;
    }

    public List getStock() {
        return this.stock;
    }

    public void setStock(List stock) {
        this.stock = stock;
    }
}
